def numpyFunc(arg1: int, arg2: str) -> str:
    """
    Demonstrate a NumPy-style docstring with dashed section headers.

    Parameters
    ----------
    arg1 : int
        First argument.
    arg2 : str
        Second argument.

    Returns
    -------
    str
        A combined string.
    """
    return f'{arg1}-{arg2}'
